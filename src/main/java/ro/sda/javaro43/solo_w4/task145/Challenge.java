package ro.sda.javaro43.solo_w4.task145;

public class Challenge {
    public static int bitwiseAND(int n1, int n2) {
        return n1 & n2;
    }

    public static int bitwiseOR(int n1, int n2) {
        return n1 | n2;
    }

    public static int bitwiseXOR(int n1, int n2) {
        return n1 ^ n2;
    }
}